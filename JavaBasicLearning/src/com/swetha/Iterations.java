package com.swetha;

public class Iterations {

	public static void main (String args[]) {
		/*
		 * Iterations - loops
		 * 1. while
		 * 2. do while
		 * 3. for
		 * 4. for each - enhanced for loop
		 */
		
		int n = 1;
		
		//while 3 times
		while(n<4){
			System.out.println("in While");
			n++;
		}
		
		
		n=1;
		// do while 4 times
		do{
			System.out.println("in Do While");
			n++;
		}while(n<=4);
				
				
		//for 5 times
		for(int i=0;i<n;i++)
		{
			System.out.println("in For");
		}
		
		//enhanced for loop - arrays and collections
		//for each streams
		
		
				
		
	}
	
}
